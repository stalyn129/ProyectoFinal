    public static String Calcular_cod_Representante(ObjectContainer Base) {

        boolean rest = true;
        int aumento = 0;
        String cod;
        do {

            aumento++;

            cod = String.format("R%04d", aumento);

            if (Verificar_codRepre(Base, cod) == 0) {
                rest = false;
            }

        } while (rest);

        return cod;
    }

