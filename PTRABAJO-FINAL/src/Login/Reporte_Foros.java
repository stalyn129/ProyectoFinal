    private void ConsultarRegistro(ObjectContainer Base, String consulta, int tipoConsulta) {
        // Creando un objeto de ejemplo para la consulta
        Comentario ejemploConsulta = new Comentario(null, null, null, null, null, null);
        Foro ejemploCons = new Foro(null, null, null, null);
        // Consultando la base de datos
        ObjectSet result;

        try {
            // Verificar el tipo de consulta para decidir qu√© campo usar
            if (tipoConsulta == 0) {
                // B√∫squeda por c√≥digo
                ejemploConsulta.setID_Comentario(consulta);
                result = Base.queryByExample(ejemploConsulta);
            } else if (tipoConsulta == 1) {
                // B√∫squeda por nombre de usuario
                ejemploConsulta.setNombre_Usario(consulta);
                result = Base.queryByExample(ejemploConsulta);
            } else if (tipoConsulta == 2) {
                // B√∫squeda por t√≠tulo de foro
                Foro foroEjemplo = new Foro();
                foroEjemplo.setTitulo_Foro(consulta);
                result = Base.queryByExample(ejemploCons);
            } else {
                // Tipo de consulta no v√°lido
                JOptionPane.showMessageDialog(this, "Tipo de consulta no v√°lido");
                return;
            }

            if (result.hasNext()) {
                // Mostrar o procesar los registros encontrados
                List<Comentario> registrosConsultados = new ArrayList<>();
                while (result.hasNext()) {
                    Object objConsultado = result.next();

                    if (objConsultado instanceof Comentario) {
                        Comentario registroConsultado = (Comentario) objConsultado;
                        System.out.println("Registro consultado (Comentario): " + registroConsultado);
                        registrosConsultados.add(registroConsultado);
                    } else if (objConsultado instanceof Foro) {
                        // Puedes manejar los registros de tipo Foro si es necesario
                        Foro foroConsultado = (Foro) objConsultado;
                        System.out.println("Registro consultado (Foro): " + foroConsultado);
                    }
                }
                JOptionPane.showMessageDialog(this, "Registros consultados con √©xito");

                // Llamar al m√©todo ConsultarDatos pasando la lista de registros consultados
                ConsultarDatos(Base, registrosConsultados);
            } else {
                JOptionPane.showMessageDialog(this, "No se encontraron registros en la base de datos");
            }
        } catch (Db4oIOException ex) {
            // Manejar la excepci√≥n aqu√≠, puedes mostrar un mensaje de error o realizar otras acciones
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al realizar la consulta en la base de datos");
        }
    }

    private void ConsultarDatos(ObjectContainer Base, List<Comentario> registrosConsultados) {
        DefaultTableModel modelo = (DefaultTableModel) jTableComentarios.getModel();
    modelo.setRowCount(0);

    if (!registrosConsultados.isEmpty()) {
        for (Comentario registroConsultado : registrosConsultados) {
            String nombreForo = obtenerTituloForo(Base, registroConsultado.getFK_Cod_Foro());
            modelo.addRow(new Object[]{
                registroConsultado.getID_Comentario(),
                registroConsultado.getNombre_Usario(),
                nombreForo,
                registroConsultado.getContenido_Comen(),
                registroConsultado.getFecha_Comen()
            });
        }
    } else {
        JOptionPane.showMessageDialog(this, "No se encontraron registros en la base de datos");
    }
    }

    private String obtenerTituloForo(ObjectContainer Base, String codigoForo) {
        Foro foroEjemplo = new Foro();
        foroEjemplo.setCod_Foro(codigoForo);

        try {
            ObjectSet result = Base.queryByExample(foroEjemplo);

            if (result.hasNext()) {
                Foro foroEncontrado = (Foro) result.next();
                return foroEncontrado.getTitulo_Foro();
            } else {
                return "T√≠tulo no encontrado";
            }
        } catch (Db4oIOException ex) {
            ex.printStackTrace();
            return "Error al obtener el t√≠tulo del foro";
        }

    }

    public void MostrarDatoComentarios(ObjectContainer Base) {
        Comentario Coment = new Comentario();
        ObjectSet result = Base.get(Coment);

        DefaultTableModel modelo = (DefaultTableModel) jTableComentarios.getModel();
        modelo.setRowCount(0);

        while (result.hasNext()) {
            Comentario miComentario = (Comentario) result.next();
            String nombreForo = obtenerTituloForo(Base, miComentario.getFK_Cod_Foro());

            modelo.addRow(new Object[]{
                miComentario.getID_Comentario(),
                miComentario.getNombre_Usario(),
                nombreForo,
                miComentario.getContenido_Comen(),
                miComentario.getFecha_Comen()
            });
        }
    }

    private void EliminarRegistroComent(ObjectContainer base, String Cod_Com) {

        Comentario cuen = new Comentario(Cod_Com, null, null, null, null, null);

        // Mensaje de depuraci√≥n
        System.out.println("Buscando el registro en la base de datos...");

        ObjectSet result = base.queryByExample(cuen);

        if (result.hasNext()) {
            // Mensaje de depuraci√≥n
            System.out.println("Eliminando el registro de la base de datos...");

            base.delete(result.next());
            JOptionPane.showMessageDialog(this, "El registro ha sido eliminado con √©xito");
            MostrarDatoComentarios(base);
        } else {
            JOptionPane.showMessageDialog(this, "No se encontr√≥ el registro en la base de datos");
        }
    }
    
   public Map<String, Integer> contarParticipacionesPorForo(ObjectContainer Base) {
    Map<String, Integer> participacionesPorForo = new HashMap<>();

    ObjectSet result = Base.get(new Comentario());
    while (result.hasNext()) {
            Comentario comentario = (Comentario) result.next();
            String codForo = comentario.getFK_Cod_Foro();
            String tituloForo = obtenerTituloForo(Base, codForo);

            participacionesPorForo.put(tituloForo, participacionesPorForo.getOrDefault(tituloForo, 0) + 1);
        }

    return participacionesPorForo;
}
   
   public void mostrarGraficoParticipacionesForos(ObjectContainer Base, JPanel panelGrafico) {
    try {
        Map<String, Integer> participacionesPorForo = contarParticipacionesPorForo(Base);

        DefaultPieDataset dataset = new DefaultPieDataset();

        for (Map.Entry<String, Integer> entry : participacionesPorForo.entrySet()) {
            dataset.setValue(entry.getKey(), entry.getValue());
        }

        JFreeChart graficoPastel = ChartFactory.createPieChart("Participaciones en Foros", dataset, true, true, false);

        ChartPanel chartPanel = new ChartPanel(graficoPastel);
        chartPanel.setMouseWheelEnabled(true);
        chartPanel.setPreferredSize(new Dimension(261, 310));

        panelGrafico.setLayout(new BorderLayout());
        panelGrafico.add(chartPanel, BorderLayout.NORTH);

        panelGrafico.revalidate();
        panelGrafico.repaint();
    } catch (Db4oIOException ex) {
        ex.printStackTrace();
        // Manejar la excepci√≥n seg√∫n tus necesidades
        throw ex; // Re-lanzar la excepci√≥n para que se maneje en el lugar donde se invoca este m√©todo
    }
}

    
    
    
